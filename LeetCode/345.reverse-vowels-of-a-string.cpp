// @lc code=start
class Solution {
public:
string reverseVowels(string s) {
    vector<char> b;
        for(int i=0;i<s.length();i++){
            if(s[i]=='a'||s[i]=='e'||s[i]=='i'||s[i]=='o'||s[i]=='u'||s[i]=='A'||s[i]=='E'||s[i]=='I'||
            s[i]=='O'||s[i]=='U') {
                b.push_back(s[i]);
            }
        }
        int n=b.size();
        for(int i=0;i<s.length();i++){
            if(s[i]=='a'||s[i]=='e'||s[i]=='i'||s[i]=='o'||s[i]=='u'||s[i]=='A'||s[i]=='E'||s[i]=='I'||s[i]=='O'||s[i]=='U') {
            s[i]=b[n-1];
                n--;
            }
        }
        return s;
    }
};
